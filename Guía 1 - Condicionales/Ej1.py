'''
1) Calcular el sueldo de una persona, conociendo la cantidad de horas que trabaja en el mes 
y el valor de la hora. 
'''

valorDeLaHora = int(input("Ingresá el valor de una hora de trabajo: "))
horasMensuales = int(input("Ingresá cuántas horas mensuales trabaja: "))

sueldoMensual = valorDeLaHora * horasMensuales

print(f"El sueldo mensual de la misma persona es: {sueldoMensual}")