'''
2) Calcular el importe que debe pagar una persona compra una heladera de pesos X y por 
pagar en efectivo le hacen el 10% de descuento ¿Cuánto abona? 
'''

valorDeLaHeladera = int(input("Ingresé el valor total de la heladera: "))

